package com.hand.Exam2;

import java.util.Scanner;

public class App 
{
    public static void main( String[] args )
    {
    	System.out.print("请输入工资：");    	
    	Scanner c1=new Scanner(System.in);   	
    	double sal=c1.nextDouble();
    	c1.close();
    	double m=sal-3500;
    	double s=0;
    	if(m<0){
    		s=0;
    	}
    	else if((0<m)&&(m<=1500)){
    		s=m*0.03;
    	}else if((1500<m)&m<=4500){
    		s=m*0.1+1500*0.03;
    		
    	}else if((4500<m)&&(m<=9000)){
    		s=(m-4500)*0.2+(m-4500-1500)*0.1+1500*0.3;   		
    	}else if((9000<m)&&(m<=35000)){
    		s=(m-9000)*0.25+(m-9000-4500)*0.2+(m-9000-4500-1500)*0.1+1500*0.03;
    		
    	}else if((35000<m)&&(m<=55000)){
    		s=(m-35000)*0.3+(m-35000-9000)*0.25+(m-35000-9000-4500)*0.2+(m-35000-9000-4500-1500)*0.1+1500*0.03;   		
    	}else if((55000<m)&&(m<=80000)){
    		s=(m-55000)*0.35+(m-55000-35000)*0.3+(m-55000-35000-9000)*0.25+(m-55000-35000-9000-4500)*0.2+(m-55000-35000-9000-4500-1500)*0.1+1500*0.03;   		
    	}else if(80000<m){
    		s=(m-80000)*0.45+(m-80000-55000)*0.35+(m-80000-55000-35000)*0.3+(m-80000-55000-35000-9000)*0.25+(m-80000-55000-35000-9000-4500)*0.2+(m-80000-55000-35000-9000-4500-1500)*0.1+1500*0.03;   		
    	}
        System.out.println( "所需缴纳的税费为:"+s );
    }
}
